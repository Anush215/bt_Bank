    private void officer_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_officer_addActionPerformed
        // TODO add your handling code here:
        int x = 0;
        String day_txt = day_off.getSelectedItem().toString();
       String month_txt =month_off.getSelectedItem().toString();
       String year_txt =year_off.getSelectedItem().toString();
       String dob_fixed =day_txt+"/"+month_txt+"/"+year_txt;
       String gender = null ;
       
       if(male_off.isSelected()){
           gender ="male";
       }else if(female_off.isSelected()){
           gender ="female";
       }
       dob_off.setText(dob_fixed);
       
       if(role_off.getSelectedItem().toString() == "Manager"){
        account_operations obj = new account_operations();
        x=obj.new_admin(name_off.getText().toString(), password_off.getText().toString(), dob_fixed, gender, address_off.getText().toString(), nic_off.getText().toString(), mobile_off.getText().toString(), role_off.getSelectedItem().toString());
       }else if(role_off.getSelectedItem().toString() == "Cashier"){
           account_operations obj = new account_operations();
        x=obj.new_cashier(name_off.getText().toString(), password_off.getText().toString(), dob_fixed, gender, address_off.getText().toString(), nic_off.getText().toString(), mobile_off.getText().toString(), role_off.getSelectedItem().toString());
      
       }
       if(x==1){
           JOptionPane.showMessageDialog(null, "'"+name_off.getText().toString()+"' added to system succesfully as a '"+role_off.getSelectedItem().toString()+"'");
       }else{
           JOptionPane.showMessageDialog(null, "Something went wrong!!! check and retry");
       }
       
    }//GEN-LAST:event_officer_addActionPerformed

