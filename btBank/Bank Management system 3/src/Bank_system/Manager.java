    private void acc_create_butActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acc_create_butActionPerformed
       
       String day_txt = day.getSelectedItem().toString();
       String month_txt =month.getSelectedItem().toString();
       String year_txt =year.getSelectedItem().toString();
       String dob_fixed =day_txt+"/"+month_txt+"/"+year_txt;
       String gender = null ;
       
       if(male_radio.isSelected()){
           gender ="male";
       }else if(female_radio.isSelected()){
           gender ="female";
       }
       dob_txt.setText(dob_fixed);
       
        account_operations obj = new account_operations();
        int x=obj.new_customer(cus_name_txt.getText().toString(), acc_type_combo.getSelectedItem().toString(), dob_fixed, gender, address_txt.getText().toString(), nic_txt.getText().toString(), mobile_txt.getText().toString(), Double.parseDouble(amount_txt.getText()));
        
        if(x==1){
            JOptionPane.showMessageDialog(null, "'"+cus_name_txt.getText().toString()+"' added to system succesfully");
        }else{
            JOptionPane.showMessageDialog(null, "something went wrong!!!");
        }
    }//GEN-LAST:event_acc_create_butActionPerformed

